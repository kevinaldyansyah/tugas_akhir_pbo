    private void showReports() {

        connection = DBHelper.getConnection();

        model = new DefaultTableModel();
        this.tableLaporanTransaksi.setModel(model);
        model.addColumn("ID Transaksi");
        model.addColumn("Nama Pelanggan");
        model.addColumn("Merk");
        model.addColumn("Jumlah");
        model.addColumn("Total Harga");
        model.addColumn("Tanggal");
        fetchDataReports();
        tableLaporanTransaksi.setVisible(true);

    }

    private void fetchDataReports() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        Object[] o = new Object[6];
        for (Transaksi t : listPembelianSepeda) {
            o[0] = t.getIdTransaksi();
            o[1] = t.getPelanggan().getNama();
            o[2] = t.getSepeda().getMerk();
            o[3] = t.getJumlah();
            o[4] = t.getTotalHarga();
            o[5] = t.getTanggal();

            model.addRow(o);
        }
    }

    private void showAllBicycle() {
        String[] column = {"ID Sepeda", "Merk", "Jenis", "Keterangan", "Harga"};
        ArrayList<Object> listBicyle = new Sepeda().getAll();

        Object[] rowData = new Object[5];

        tabelCari.setModel(new DefaultTableModel(new Object[][]{}, column));

        for (Object o : listBicyle) {
            Sepeda sepeda = (Sepeda) o;

            rowData[0] = sepeda.getIdSepeda();
            rowData[1] = sepeda.getMerk();
            rowData[2] = sepeda.getJenis();
            rowData[3] = sepeda.getKeterangan();
            rowData[4] = sepeda.getTotalHarga();

            ((DefaultTableModel) tabelCari.getModel()).addRow(rowData);

        }
        tabelCari.setPreferredScrollableViewportSize(tabelCari.getPreferredSize());
        tabelCari.setFillsViewportHeight(true);
    }

    private void showCart() {
        String[] column = {"ID Sepeda", "Merk", "Jenis", "Keterangan", "Harga", "Jumlah", "Total Harga"};

        Object[] rowData = new Object[7];

        tabelPembelian.setModel(new DefaultTableModel(new Object[][]{}, column));

        int totalHarga = 0;

        for (Transaksi t : listPembelianSepeda) {
            rowData[0] = t.getSepeda().getIdSepeda();
            rowData[1] = t.getSepeda().getMerk();
            rowData[2] = t.getSepeda().getJenis();
            rowData[3] = t.getSepeda().getKeterangan();
            rowData[4] = t.getSepeda().getTotalHarga();
            rowData[5] = t.getJumlah();
            rowData[6] = t.getJumlah() * t.getSepeda().getTotalHarga();
            totalHarga += t.getJumlah() * t.getSepeda().getTotalHarga();
            ((DefaultTableModel) tabelPembelian.getModel()).addRow(rowData);
        }

        txtTotalHarga.setText(String.valueOf(totalHarga));
    }

