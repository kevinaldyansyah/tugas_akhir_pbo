    private void btnMasukkanKeranjangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMasukkanKeranjangActionPerformed

        String[] column = {"ID Sepeda", "Merk", "Jenis", "Keterangan", "Harga", "Jumlah", "Total Harga"};

        tabelPembelian.setModel(new DefaultTableModel(new Object[][]{}, column));

        int jumlahSepeda = Integer.parseInt(txtJumlahSepeda.getText());
        int idSepeda = Integer.parseInt(txtIdSepeda.getText());

        Sepeda sepeda = (Sepeda) new Sepeda().getById(idSepeda);

        Pelanggan pelanggan = new Pelanggan(txtNamaPelanggan.getText());

        Transaksi transaksi = new Transaksi(pelanggan, sepeda, jumlahSepeda, jumlahSepeda * sepeda.getTotalHarga(), txtTanggalReceiver.getText());
        listPembelianSepeda.add(transaksi);

        showCart();
    }//GEN-LAST:event_btnMasukkanKeranjangActionPerformed

    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        ArrayList<Object> searchResults = new Sepeda().search(txtCari.getText());

        String[] column = {"ID Sepeda", "Merk", "Jenis", "Keterangan", "Harga"};

        Object[] rowData = new Object[5];

        tabelCari.setModel(new DefaultTableModel(new Object[][]{}, column));

        for (Object o : searchResults) {
            Sepeda sepeda = (Sepeda) o;

            rowData[0] = sepeda.getIdSepeda();
            rowData[1] = sepeda.getMerk();
            rowData[2] = sepeda.getJenis();
            rowData[3] = sepeda.getKeterangan();
            rowData[4] = sepeda.getTotalHarga();

            ((DefaultTableModel) tabelCari.getModel()).addRow(rowData);

        }

        tabelCari.setPreferredScrollableViewportSize(tabelCari.getPreferredSize());
        tabelCari.setFillsViewportHeight(true);
    }//GEN-LAST:event_btnCariActionPerformed

