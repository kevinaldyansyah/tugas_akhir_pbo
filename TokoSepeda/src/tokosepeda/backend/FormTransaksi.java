    private void btnCariPelangganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariPelangganActionPerformed
        // TODO add your handling code here:
        connection = DBHelper.getConnection();
        
        DefaultTableModel model = new DefaultTableModel();
        this.tableRiwayatBelanja.setModel(model);
        model.addColumn("ID Transaksi");
        model.addColumn("Merk");
        model.addColumn("Jumlah");
        model.addColumn("Total Harga");
        model.addColumn("Tanggal");
        
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            Statement s = connection.createStatement();
            Pelanggan pelanggan = (Pelanggan) new Pelanggan().search(txtCariPelanggan.getText()).get(0);
            String sql = "Select * from	transaksi where id_pelanggan LIKE '%" + pelanggan.getIdPelanggan() + "%'";
            ResultSet r = s.executeQuery(sql);
            while (r.next()) {
                Object[] o = new Object[5];
                
                Sepeda sepeda = (Sepeda) new Sepeda().getById(r.getInt("id_sepeda"));
                
                o[0] = r.getString("id_transaksi");
                o[1] = sepeda.getMerk();
                o[2] = r.getString("jumlah");
                o[3] = r.getString("total_harga");
                o[4] = r.getString("tanggal");

                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan " + e.getMessage());
        }
    }//GEN-LAST:event_btnCariPelangganActionPerformed

